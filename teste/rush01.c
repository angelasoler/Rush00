#include <unistd.h>


void ft_putchar(char *c);

void rush(int *x)
{
	
	
	char a[2];

	a[0] = '1';
	a[1] = 92;
	
	while (a[0] <= &x)
	{
		if (a[0] == '1')
		{
			ft_putchar('/');
		}
		if (a[0] == &x)
		{
			ft_putchar(&a[1]);
		}
		if (a[0] != '1' && a[0] != &x)
		{
			ft_putchar('*');
		}
		a[0]++;
	}
}
